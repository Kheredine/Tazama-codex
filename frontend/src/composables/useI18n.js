export function useI18n() {
  const t = computed(() => translations[lang.value] || translations.en)

  const setLang = (l) => {
    lang.value = l
    localStorage.setItem('tazama_lang', l)
  }

  const toggleLang = () => setLang(lang.value === 'en' ? 'fr' : 'en')

  return { lang, t, setLang, toggleLang }
}
